class FoodItem {
  final String id;
  final String name;
  final String image;
  final String description;
  final double price;
  final double rating;
  final bool favourite;

  FoodItem({
    required this.id,
    required this.name,
    required this.image,
    required this.description,
    required this.price,
    required this.rating,
    required this.favourite,
  });
}
